{{-- Por qué nace el proyecto --}}
<div class="pb-4 mb-5 mt-5">
    <div>
        <h2>AI Automations Experimental</h2>
    </div>

    <div class="mb-4 mt-4">
        <ul class="r-list-checked">
            <li>Generate an idea based on the role system</li>
            <li>Auto-generated prompt from the generated idea</li>
            <li>Stable diffusion API connection or Dall-e to generate a batch of images with the <strong>PROMPT</strong></li>
            <li>Publication of the batch of images to my website and on social networks</li>
            <li>Creation of slideshow with transitions and music using ffmpeg</li>
            <li>Publishing videos on video platforms</li>
        </ul>
    </div>

    <div>
        <p>
            The objective of this project is to generate an "idea" based on a system of roles with which later
            finish generating a prompt using GPT3.5 Instruct.
        </p>

        <p>
            This prompt will be used to generate a batch/collection of images using the Stable Diffusion API
            although it is also possible to use Dall-e.
        </p>


        <p>
            Once a batch of images is generated they will be uploaded to my own
            <a rel="me" href="{{route('home')}}" target="_blank">
                API
            </a>
            to be stored as a gallery.
        </p>

        <p>
            Subsequently, the images will be published on the integrated social networks.
            Mostly:

            <a rel="me" href="https://twitter.com/ai_automations" target="_blank">
                Twitter
            </a>,

            <a rel="me" href="https://www.instagram.com/ai_automations_experimental" target="_blank">
                Instagram
            </a>,

            <a rel="me" href="https://mastodon.online/@ai_automations_experimental" target="_blank">
                Mastodon
            </a>,

            <a rel="me" href="https://t.me/ai_automations_experimental" target="_blank">
                Telegram
            </a>
        </p>

        <p>
            Then, with all those images and using the <strong>ffmpeg</strong> video tool, you create
            automatically a slideshow with transitions between images and background music.
        </p>

        <p>
            This video will be automatically uploaded to
            <a rel="me" href="https://www.tiktok.com/@ai_automations" target="_blank"><strong>tiktok</strong></a>
            with selenium and
            <a rel="me" href="https://www.youtube.com/@ai_automations_experimental" target="_blank">
                <strong>Youtube</strong>
            </a>
            using your api and it will also be synchronized by sending a request to my api so that the video is set as
            collection/gallery cover.
        </p>
    </div>
</div>

